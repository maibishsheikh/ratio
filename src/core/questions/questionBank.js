/**
 * Get questions for a specific world and concept tag.
 */
export function getQuestionsByTag(tag) {
  return questionBank.filter(q => q.tag === tag);
}

/**
 * Get all questions for a world.
 */
export function getQuestionsByWorld(worldId) {
  return questionBank.filter(q => q.world === worldId);
}

/**
 * Get a random subset of n questions for a given world, shuffled.
 */
export function getRandomQuestions(worldId, n = 10, excludeIds = []) {
  const pool = questionBank
    .filter(q => q.world === worldId && !excludeIds.includes(q.id));
  const shuffled = pool.sort(() => Math.random() - 0.5);
  return shuffled.slice(0, Math.min(n, shuffled.length));
}

/**
 * Get a specific question by ID.
 */
export function getQuestionById(id) {
  return questionBank.find(q => q.id === id);
}

/**
 * Parse options string to array.
 */
export function parseOptions(question) {
  if (Array.isArray(question.options)) return question.options;
  return question.options.split(',').map(o => o.trim());
}
